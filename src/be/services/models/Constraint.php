<?php

namespace PROJECT\Services;

class Constraint
{

    private $key;

    private $constraint;

    /**
     * Constructor
     *
     */
    public function __construct($key, $constraint)
    {
        $this->key        = $key;
        $this->constraint = $constraint;
    }

    public function getKey()
    {
        return $this->key;
    }

    public function getKey()
    {
        return $this->key;
    }
}

